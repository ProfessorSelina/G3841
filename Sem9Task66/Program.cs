// Задача 66: Задайте значения M и N. 
// Напишите программу, которая найдёт сумму 
// натуральных элементов в промежутке от M до N.
// M = 1; N = 15 -> 120 M = 4; N = 8. -> 30

using System;

class Program
{
    static void Main(string[] args)
    {
        // задаем значение M
        int m = 1; 
        // задаем значение N
        int n = 15; 

        // вычисляем сумму
        int sum = SumInRange(m, n); 

        // выводим результат на экран
        Console.WriteLine($"Сумма натуральных чисел от {m} до {n} равна {sum}");
    }

    // рекурсивная функция для вычисления суммы натуральных чисел в промежутке от m до n
    static int SumInRange(int m, int n)
    {
        // базовый случай: если m и n равны, то возвращаем m
        if (m == n) 
        {
            return m;
        }
        // рекурсивный случай: вычисляем сумму для промежутка от m до n-1 и добавляем n
        else 
        {
            return SumInRange(m, n - 1) + n;
        }
    }
}